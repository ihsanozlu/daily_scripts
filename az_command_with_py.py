from azure.cli.core import get_default_cli


def az_cli(args_str):
    args = args_str.split()
    cli = get_default_cli()
    cli.invoke(args)
    if cli.result.result:
        return cli.result.result
    elif cli.result.error:
        raise cli.result.error
    return True


response = az_cli("vm list")
print("vm's: %s" % (response))

